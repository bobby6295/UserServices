package com.demo.DTO;

public class Product {
    private String code;

    private Long id;

    private String name;

    private String description;

    private double price;

    public String getCode() {
        return code;
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public double getPrice() {
        return price;
    }
}